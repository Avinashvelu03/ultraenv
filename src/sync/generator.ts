/**
 * Check if a .env.example file needs updating by comparing its content
 * with what would be generated from the current .env file.
 */
export async function needsUpdate(
  envPath: string,
  examplePath: string,
  options?: GenerateExampleOptions,
): Promise<boolean> {
  const envExists = await exists(envPath);
  const exampleExists = await exists(examplePath);

  // If env exists but example doesn't, update is needed
  if (envExists && !exampleExists) {
    return true;
  }

  // If neither exists, no update needed
  if (!envExists) {
    return false;
  }

  const content = await readFile(envPath);
  const schema = options?.schemaPath !== undefined
    ? await loadSchema(options.schemaPath)
    : undefined;

  const parsed = parseEnvFile(content, envPath);
  const generated = generateExampleContent(parsed.vars, schema, {
    includeDescriptions: options?.includeDescriptions ?? true,
    includeTypes: options?.includeTypes ?? true,
    includeDefaults: options?.includeDefaults ?? true,
  });

  const existing = await readFile(examplePath);
  return generated.trim() !== existing.trim();
}
