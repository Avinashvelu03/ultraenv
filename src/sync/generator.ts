/**
 * Generate a .env.example file from a .env file.
 *
 * Reads the .env file, strips secret values, preserves comments and variable
 * ordering, and optionally adds type hints, descriptions, and default values
 * from a schema definition.
 *
 * @param envPath - Path to the source .env file
 * @param outputPath - Path where the .env.example will be written
 * @param options - Generation options
 * @throws FileSystemError if the .env file cannot be read
 */
export async function generateExampleFile(
  envPath: string,
  outputPath: string,
  options?: GenerateExampleOptions,
): Promise<void> {
  const includeDescriptions = options?.includeDescriptions ?? true;
  const includeTypes = options?.includeTypes ?? true;
  const includeDefaults = options?.includeDefaults ?? true;

  const content = await readFile(envPath);
  const schema = options?.schemaPath !== undefined
    ? await loadSchema(options.schemaPath)
    : undefined;

  const parsed = parseEnvFile(content, envPath);
  const generated = generateExampleContent(parsed.vars, schema, {
    includeDescriptions,
    includeTypes,
    includeDefaults,
  });

  await writeFile(outputPath, generated);
}

/**
 * Generate the content of a .env.example file from parsed variables.
 *
 * @param vars - Parsed environment variables from a .env file
 * @param schema - Optional schema definition for type/description metadata
 * @param options - Generation options
 * @returns The complete .env.example file content as a string
 */
export function generateExampleContent(
  vars: readonly ParsedEnvVar[],
  schema?: SchemaDefinition,
  options?: {
    includeDescriptions?: boolean;
    includeTypes?: boolean;
    includeDefaults?: boolean;
  },
): string {
  const includeDescriptions = options?.includeDescriptions ?? true;
  const includeTypes = options?.includeTypes ?? true;
  const includeDefaults = options?.includeDefaults ?? true;

  const lines: ExampleLine[] = [];
  let order = 0;

  // Track which variables have been emitted (to avoid duplicates)
  const emittedKeys = new Set<string>();

  // Add header
  lines.push({ content: HEADER, order: order++ });

  for (const envVar of vars) {
    if (emittedKeys.has(envVar.key)) continue;
    emittedKeys.add(envVar.key);

    const schemaEntry = schema !== undefined ? schema[envVar.key] : undefined;
    const schemaRecord = schemaEntry !== undefined ? schemaEntry as unknown as Record<string, unknown> : undefined;
    const isRequired = schemaEntry === undefined || schemaEntry.optional !== true;
    const isSecret = isSecretVariable(envVar.key, envVar.value, schemaEntry);

    // Build comment block above the variable
    const commentLines: string[] = [];

    // Description comment
    if (includeDescriptions && schemaEntry?.description !== undefined) {
      commentLines.push(`# ${schemaEntry.description}`);
    }

    // Type hint comment
    if (includeTypes && schemaEntry !== undefined) {
      const typeStr = schemaTypeToString(schemaEntry);
      commentLines.push(`# type: ${typeStr}`);

      // Enum values
      const enumValues = extractEnumValues(schemaEntry);
      if (enumValues !== null) {
        commentLines.push(`# values: ${enumValues.join(', ')}`);
      }
    }

    // Secret marker
    if (isSecret) {
      commentLines.push('# [SECRET]');
    }

    // Required/optional marker
    if (includeDescriptions) {
      if (isRequired && schemaRecord?.default === undefined) {
        commentLines.push('# required');
      } else if (schemaRecord?.default !== undefined) {
        commentLines.push('# optional');
      } else if (schemaEntry?.optional === true) {
        commentLines.push('# optional');
      }
    }

    // Default value
    if (includeDefaults) {
      if (schemaRecord?.default !== undefined) {
        const defaultDisplay = isSecret
          ? '[REDACTED]'
          : String(schemaRecord.default);
        commentLines.push(`# default: ${defaultDisplay}`);
      } else if (schemaRecord?.hasDefault === true && typeof schemaRecord.rawDefaultValue === 'string') {
        const defaultDisplay = isSecret
          ? '[REDACTED]'
          : schemaRecord.rawDefaultValue;
        commentLines.push(`# default: ${defaultDisplay}`);
      }
    }

    // Push comment lines
    for (const commentLine of commentLines) {
      lines.push({ content: commentLine, order: order++ });
    }

    // Push the variable line with stripped value
    const exampleValue = buildExampleValue(envVar.value, isSecret, schemaEntry);
    lines.push({
      content: `${envVar.key}=${exampleValue}`,
      order: order++,
    });

    // Add blank line between variables
    lines.push({ content: '', order: order++ });
  }

  // Sort by original order and build final content
  lines.sort((a, b) => a.order - b.order);
  return lines.map((line) => line.content).join('\n');
}

