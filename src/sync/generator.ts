// =============================================================================
// ultraenv — .env.example Generator
// Reads .env files and produces safe .env.example files with stripped secrets,
// type hints, descriptions, and required/optional markers.
// =============================================================================

import type { ParsedEnvVar, SchemaDefinition } from '../core/types.js';
import { parseEnvFile } from '../core/parser.js';
import { readFile, writeFile, exists } from '../utils/fs.js';
import { isSecretKey, isSecretLike, maskValue } from '../utils/mask.js';

// -----------------------------------------------------------------------------
// Types
// -----------------------------------------------------------------------------

export interface GenerateExampleOptions {
  /** Include type hint comments (default: true) */
  includeDescriptions?: boolean;
  /** Include type annotation comments (default: true) */
  includeTypes?: boolean;
  /** Include default values in comments (default: true) */
  includeDefaults?: boolean;
  /** Path to a schema file (TypeScript module exporting schema) for metadata */
  schemaPath?: string;
}

interface ExampleLine {
  /** The text content of the line (may be comment or variable) */
  content: string;
  /** The original line number for ordering */
  order: number;
}

// -----------------------------------------------------------------------------
// Constants
// -----------------------------------------------------------------------------

const HEADER = [
  '# ============================================================',
  '# Auto-generated by ultraenv — DO NOT EDIT',
  '# Copy this file to .env and fill in the values',
  '# ============================================================',
  '',
].join('\n');

// -----------------------------------------------------------------------------
// Schema Type Mapping
// -----------------------------------------------------------------------------

type SchemaEntry = SchemaDefinition[string];

/** Map schema type to a human-readable comment string */
function schemaTypeToString(schema: SchemaEntry): string {
  const schemaRecord = schema as unknown as Record<string, unknown>;
  if ('type' in schemaRecord) {
    const schemaType = schemaRecord.type as string;
    switch (schemaType) {
      case 'string': return 'string';
      case 'number': return 'number';
      case 'boolean': return 'boolean';
      case 'enum': return 'enum';
      case 'array': return 'array (comma-separated)';
      case 'json': return 'json';
      case 'date': return 'date (ISO 8601)';
      case 'bigint': return 'bigint';
      default: return 'string';
    }
  }
  return 'string';
}

/** Extract enum values from an enum schema for display */
function extractEnumValues(schema: SchemaEntry): string[] | null {
  const schemaRecord = schema as unknown as Record<string, unknown>;
  if (schemaRecord.type === 'enum') {
    const values = schemaRecord.values;
    if (values !== undefined && Array.isArray(values)) {
      return [...(values as readonly string[])];
    }
  }
  if (schemaRecord.type === 'string') {
    const enumValues = schemaRecord.enum;
    if (enumValues !== undefined && Array.isArray(enumValues)) {
      return [...(enumValues as readonly string[])];
    }
  }
  return null;
}

// -----------------------------------------------------------------------------
// Secret Detection
// -----------------------------------------------------------------------------

/**
 * Determine if a variable should be treated as a secret based on
 * schema metadata, key name heuristics, and value patterns.
 */
function isSecretVariable(key: string, value: string, schema?: SchemaEntry): boolean {
  // Check schema metadata for secret marker
  if (schema !== undefined) {
    const schemaRecord = schema as unknown as Record<string, unknown>;
    if (schemaRecord.isSecret === true) {
      return true;
    }
  }
  // Check key name heuristics
  if (isSecretKey(key)) {
    return true;
  }
  // Check value pattern heuristics (only for non-empty values)
  if (value.length > 0 && isSecretLike(value)) {
    return true;
  }
  return false;
}

