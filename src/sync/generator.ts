// Helpers
// -----------------------------------------------------------------------------

/**
 * Build the example value for a variable.
 * Secrets are replaced with a placeholder, other values get a generic example.
 */
function buildExampleValue(
  value: string,
  isSecret: boolean,
  schema?: SchemaEntry,
): string {
  if (isSecret) {
    // For secrets, provide a generic placeholder
    return '<your-secret-here>';
  }

  // Check for enum — show first option
  if (schema !== undefined) {
    const enumValues = extractEnumValues(schema);
    if (enumValues !== null && enumValues.length > 0) {
      return enumValues[0] ?? '';
    }
  }

  // Empty value — leave it empty
  if (value === '') {
    return '';
  }

  // For non-secret values, provide a masked placeholder that preserves format hints
  if (value.length <= 20 && !isSecretLike(value)) {
    // Short non-secret values could be safe to show as-is for documentation
    // but we mask them anyway for safety
    return maskValue(value, { visibleStart: 0, visibleEnd: 0, minLength: 1 });
  }

  return maskValue(value, { visibleStart: 0, visibleEnd: 0, minLength: 1 });
}

