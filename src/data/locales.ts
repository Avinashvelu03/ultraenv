/** Pre-computed Set for O(1) locale lookups */
const LOCALE_SET = new Set<string>(LOCALES);

/**
 * Check whether a given string is a recognized locale / language tag.
 *
 * @param locale - The locale string to validate (e.g., "en", "en-US", "zh-Hans-CN")
 * @returns `true` if the locale is in the known locales list
 *
 * @example
 * ```ts
 * isValidLocale('en');       // true
 * isValidLocale('en-US');    // true
 * isValidLocale('zh-CN');    // true
 * isValidLocale('fr-FR');    // true
 * isValidLocale('xx-YY');    // false
 * ```
 */
export function isValidLocale(locale: string): boolean {
  return LOCALE_SET.has(locale);
}
